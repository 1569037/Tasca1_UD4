public class Ejercicio2App {
	public static void main(String[] args) {
		
		//Declaracion de variables y asignacion de valores
		int n = 50;
		double a = 21.2;
		char c = 'l';
		
		//Muestra por pantalla el varol de las variables anteriores
		
		System.out.println("Variable N = : "+n);
		System.out.println("Variable A = : "+a);
		System.out.println("Variable C = : "+c);
		
		//Muestra por pantalla de operaciones realizadas con las variables anteriores
		
		System.out.println(n+" + "+a+" = "+(n+a));
		System.out.println(a+" - "+n+" = "+(a-n));
		System.out.println("Valor numerico del caracter c = "+(int)c);
		
		
		
		
		
		

	}

}
