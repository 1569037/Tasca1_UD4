public class Ejercicio1App {
	public static void main(String[] args) {
		//Declaración de variables
		
		int n1 = 40;
		int n2 = 21;
		
		//Declaracion de variables que operan
		
		int suma = n1+n2;
		int resta = n1-n2;
		int mul = n1*n2;
		int div = n1/n2;
		int mod = n1%n2;
		
		//Muesra por pantalla de los resultados obtenidos
		
		System.out.println("El resultado de la suma de los valores n1 y n2 es: "+suma);
		System.out.println("El resultado de la resta de los valores n1 y n2 es: "+resta);
		System.out.println("El resultado de la multiplicacion de los valores n1 y n2 es: "+mul);
		System.out.println("El resultado de la division de los valores n1 y n2 es: "+div);
		System.out.println("El resultado del modulo de los valores n1 y n2 es: "+mod);
		
	}
}
